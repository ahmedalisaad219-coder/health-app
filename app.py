
import streamlit as st

st.set_page_config(page_title="مدربي الصحي الذكي", page_icon="💪", layout="centered")

st.title("💪 مدربك الذكي المطور")
st.write("لقد قمنا بتحديث النظام ليقدم لك خطة علاجية كاملة!")

# الإدخالات
name = st.text_input("الاسم الكريم:")
weight = st.number_input("الوزن الحالي (كجم):", min_value=30.0, value=75.0)
height = st.number_input("الطول الحالي (سم):", min_value=100.0, value=170.0)
phone_hours = st.slider("ساعات استخدام الموبايل يومياً:", 0, 15, 5)
workout_days = st.slider("كم يوم تتمرن في الأسبوع؟", 0, 7, 2)

if st.button("تحميل الخطوة العلاجية الشاملة"):
    bmi = weight / ((height/100)**2)
    st.divider()
    st.header(f"🩺 التقرير الشامل للكابتن {name}")
    
    # قسم الوزن والتحليل الغذائي
    col1, col2 = st.columns(2)
    with col1:
        st.metric("مؤشر كتلة الجسم", f"{bmi:.1f}")
    
    if bmi < 18.5:
        st.warning("📊 التشخيص: نحافة مفرطة")
        with st.expander("🍎 الخطة الغذائية لزيادة الوزن"):
            st.write("* تناول 5 وجبات يومياً.\n* ركز على: (الموز، المكسرات، زبدة الفول السوداني، التمر).\n* اشرب عصائر طبيعية بدلاً من الماء وسط الوجبات.")
    elif 18.5 <= bmi < 25:
        st.success("📊 التشخيص: وزن مثالي")
        with st.expander("🌟 خطة الحفاظ على اللياقة"):
            st.write("* استمر على نظامك المتوازن.\n* ركز على الألياف والخضروات.\n* حافظ على شرب 3 لتر ماء يومياً.")
    else:
        st.error("📊 التشخيص: زيادة في الوزن")
        with st.expander("🥗 خطة حرق الدهون (الدايت)"):
            st.write("* ابدأ الصيام المتقطع (16 ساعة صيام).\n* امنع السكر الأبيض والحلويات تماماً.\n* استبدل الخبز الأبيض بالخبز الأسمر أو الشوفان.")

    # قسم النشاط البدني والموبايل
    st.divider()
    st.subheader("🏃 الجدول الرياضي المقترح")
    if workout_days < 3:
        st.info("نحتاج لزيادة نشاطك: ابدأ بـ 20 دقيقة مشي سريع + 10 دقائق تمارين سويدي (ضغط وبطن) يوم ورا يوم.")
    else:
        st.success("أنت بطل رياضي! استمر في تمارين المقاومة لزيادة الكتلة العضلية.")

    # قسم نصيحة الموبايل والعين
    if phone_hours > 5:
        st.warning(f"⚠️ تنبيه: استخدامك للموبايل ({phone_hours} ساعات) يجهد عينك وعقلك.")
        st.write("💡 **نصيحة تقنية:** طبق قاعدة (20-20-20): كل 20 دقيقة موبايل، انظر لشيء بعيد مسافة 20 قدم لمدة 20 ثانية.")

    st.balloons()