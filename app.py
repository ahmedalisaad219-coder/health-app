# 3. واجهة المستخدم
st.title("🏥 الموسوعة الصحية الذكية")
st.write("أدخل بياناتك وسنقوم باستخراج الحلول من قاعدة بياناتنا الشاملة")

with st.sidebar:
    st.header("🔍 فحص الحالة")
    name = st.text_input("الاسم:")
    weight = st.number_input("الوزن:", 30, 200, 70)
    height = st.number_input("الطول:", 100, 250, 170)
    age = st.number_input("العمر:", 10, 80, 25)

if st.button("إصدار الحلول الصحية الشاملة"):
    bmi = weight / ((height/100)**2)
    
    # تحديد المفتاح بناءً على الـ BMI
    if bmi < 18.5: status = "نحافة"
    elif bmi < 25: status = "مثالي"
    else: status = "زيادة وزن"
    
    data = HEALTH_KNOWLEDGE[status]
    
    st.balloons()
    st.header(f"🩺 الحلول المقترحة لحالة: {status}")
    
    # عرض الحلول في مربعات منفصلة
    col1, col2, col3 = st.columns(3)
    
    with col1:
        st.success("💡 النصيحة الطبية")
        st.write(data["نصيحة"])
        
    with col2:
        st.info("🍽️ قائمة الطعام")
        for meal in data["وجبات"]:
            st.write(f"- {meal}")
            
    with col3:
        st.warning("🏋️ خطة التمرين")
        st.write(data["تمارين"])

    # إضافة قسم "اسأل المستشار" (محاكاة لقاعدة بيانات ضخمة)
    st.divider()
    st.subheader("❓ نصيحة عشوائية من الموسوعة")
    extra_tips = [
        "شرب الماء الدافئ صباحاً يحسن عملية الهضم.",
        "النوم في غرفة مظلمة تماماً يرفع جودة النوم بنسبة 30%.",
        "تناول البروتين في الفطار يقلل الجوع طوال اليوم.",
        "المشي لمدة 10 دقائق بعد الأكل يخفض سكر الدم."
    ]
    st.light(random.choice(extra_tips))